const container = document.getElementById("projectsContainer");

projects.forEach((project, index) => {
    container.innerHTML += `
        <div class="col-md-4 mb-4 project-card"
             data-category="${project.category}">
             
            <div class="card h-100 rounded-5">
                <div class="card-body">
                    <h5 class="card-title">${project.title}</h5>

                    <span class="badge badge-success p-1">
                        ${project.category}
                    </span>

                    <p class="card-text mt-2">${project.shortDesc}</p>

                    <button class="btn-download-2 btn-sm p-2"
                        onclick="openProject(${index})">
                        View Details
                    </button>
                </div>
            </div>
        </div>
    `;
});

const filterButtons = document.querySelectorAll("[data-filter]");
const projectCards = document.querySelectorAll(".project-card");

function applyFilter(filter) {
    projectCards.forEach(card => {
        const category = card.getAttribute("data-category");

        if (filter === "all" || category === filter) {
            card.style.display = "block";
        } else {
            card.style.display = "none";
        }
    });
}

filterButtons.forEach(button => {
    button.addEventListener("click", function () {

        // Active button styling
        filterButtons.forEach(btn => btn.classList.remove("active-filter"));
        this.classList.add("active-filter");

        const filter = this.getAttribute("data-filter");
        applyFilter(filter);
    });
});

/* ================= DEFAULT FILTER ================= */
document.addEventListener("DOMContentLoaded", () => {
    const defaultFilter = "Laravel";

    // Highlight Laravel button
    filterButtons.forEach(btn => {
        if (btn.getAttribute("data-filter") === defaultFilter) {
            btn.classList.add("active-filter");
        }
    });

    // Apply Laravel filter
    applyFilter(defaultFilter);
});



function openProject(index) {
    const project = projects[index];

    document.getElementById("modalTitle").innerText = project.title;
    document.getElementById("modalDescription").innerText = project.fullDesc;
    document.getElementById("modalLink").href = project.link;

    const img = document.getElementById("modalImage");
    if (project.image) {
        img.src = project.image;
        img.classList.remove("d-none");
    } else {
        img.classList.add("d-none");
    }

    $('#projectModal').modal('show');
}
